package tools;

public class BaseCalc {
    public int sum (int x, int y){
        return x + y;
    }
    public int difference (int x, int y){
        return x - y;
    }
    public int product (int x, int y){
        return x * y;
    }
    public int quotient (int x, int y){
        return x / y;
    }
}